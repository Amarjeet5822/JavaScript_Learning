// Pure function
// If you are passing same arguments and same result will come every time
// Then, you are dealing with a pure function
// Here, sum is a pure function

const sum = (a, b) => {
   return a + b;
};

console.log(sum(10, 20)); // 30
console.log(sum(20, 30)); // 50










console.log(sum(10, 20)); // 30








